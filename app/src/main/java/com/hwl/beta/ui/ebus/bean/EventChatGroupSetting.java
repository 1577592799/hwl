package com.hwl.beta.ui.ebus.bean;

public class EventChatGroupSetting {
    private String groupGuid;
    private String groupName;
    private String groupNote;
    private String groupUserRemark;

    public String getGroupGuid() {
        return groupGuid;
    }

    public void setGroupGuid(String groupGuid) {
        this.groupGuid = groupGuid;
    }

    public String getGroupName() {
        return groupName;
    }

    public void setGroupName(String groupName) {
        this.groupName = groupName;
    }

    public String getGroupNote() {
        return groupNote;
    }

    public void setGroupNote(String groupNote) {
        this.groupNote = groupNote;
    }

    public String getGroupUserRemark() {
        return groupUserRemark;
    }

    public void setGroupUserRemark(String groupUserRemark) {
        this.groupUserRemark = groupUserRemark;
    }
}
